#pragma once

#define IMX8MP_MMIO_BASE 0x30000000UL

#define IMX8MP_MMIO_AIPS1_BASE (IMX8MP_MMIO_BASE + 0x00000000UL)  // 0x3000_0000
#define IMX8MP_MMIO_AIPS2_BASE (IMX8MP_MMIO_BASE + 0x00400000UL)  // 0x3040_0000
#define IMX8MP_MMIO_AIPS3_BASE (IMX8MP_MMIO_BASE + 0x00800000UL)  // 0x3080_0000
#define IMX8MP_MMIO_AIPS5_BASE (IMX8MP_MMIO_BASE + 0x00C00000UL)  // 0x30C0_0000

/* Reserved memory between AIPS5 and AIPS4, they dont follow a lineal order */

#define IMX8MP_MMIO_AIPS4_BASE (IMX8MP_MMIO_BASE + 0x02C00000UL)  // 0x32C0_0000

#define IMX8MP_MMIO_AIPS_N_BASE(n) IMX8MP_MMIO_AIPS##n##_BASE
